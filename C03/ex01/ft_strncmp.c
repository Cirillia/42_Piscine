#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && i < n - 1)
	{
		i++;
	}
	if (s1[i] == s2[i])
	{
		return (0);
	}
	else
	{
		return (s1[i] - s2[i]);
	}
}

int	main(void)
{
	printf("%d \n", ft_strncmp("543","543210", 3));
	printf("%d \n", ft_strncmp("543","543", 3));
	printf("%d \n", ft_strncmp("abc","123", 3));
}
